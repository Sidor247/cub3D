#include "libft.h"

int	ft_isalpha(int c)
{
	unsigned char	ch;

	if (c < 0 || c > 255)
		return (0);
	ch = (unsigned char)c;
	if (ch >= 'A' && ch <= 'Z')
		return (c);
	if (ch >= 'a' && ch <= 'z')
		return (c);
	return (0);
}
