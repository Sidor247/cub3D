#include "../../cub3d.h"

void	play_sound(t_mlx *mlx, char *name)
{
	pid_t	pid;

	if (SOUND == 0)
		return ;
	pid = fork();
	if (pid == 0)
	{
		system(name);
		mlx_stop(mlx);
	}
}